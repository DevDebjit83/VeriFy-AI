// Translation function
export const t = (key: string, lang: string = 'en'): string => {
  const translation = translations[lang as keyof typeof translations];
  if (translation && translation[key as keyof typeof translation]) {
    return translation[key as keyof typeof translation];
  }
  // Fallback to English
  return translations.en[key as keyof typeof translations.en] || key;
};

// Hook for translations
export const useTranslation = (lang: string) => {
  return (key: string) => t(key, lang);
};
